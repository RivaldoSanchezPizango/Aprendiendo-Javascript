console.log(Math)

console.log(Math.PI)

// Metodos para redondear numeros

console.log(Math.abs(7.8)) // devuelve el valor absoluto de un numero (es decir da igual si es -7.8)
console.log(Math.ceil(9.8)) // redondea el numero al numero entero posterior 
console.log(Math.floor(9.8)) // redonde el numero entero menor
console.log(Math.round(7.6)) // redondea al mas numero cercano 

// Metodo para sacar la raiz cuadrada

console.log(Math.sqrt(81)) // sacar la raiz cuadrada

// Metodo para elevar potencias
console.log(Math.pow(3, 2))

// Metodo para indicar si el numero es positivo negativo o cero
console.log(Math.sign(-23.43))  // -1 o 1

console.log(Math.random() * 1000) // valor aleatorio comprendido entre 0 y 1
console.log(Math.round(Math.random() * 1000))
